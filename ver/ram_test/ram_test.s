	ORG $8000

	FILL $FF,$e000-$8000

	ORG $E000
RESET: 
	ORCC #$10
	LDX #0
	LDA #$55
	LDU #0
@LOOP:	
	STA ,X
	CMPA ,X+
	BNE ERROR_RAM
	CMPX #$2000
	BNE @LOOP

	; Character RAM
	LDA #$AA
	LDX #$2000
@LOOP:
	STA ,X
	CMPA ,X+
	BNE ERROR_CHRAM
	CMPX #$2800
	BNE @LOOP
	BRA NO_ERROR



NO_ERROR:
	LDU #0
	BRA NO_ERROR

ERROR_RAM:
	LDU #1
	BRA ERROR_RAM

ERROR_CHRAM:
	LDU #2
	BRA ERROR_CHRAM


	FILL $FF,$FFFE-*

	.WORD RESET